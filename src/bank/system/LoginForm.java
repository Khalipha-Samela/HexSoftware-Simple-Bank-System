   private boolean authenticateUser(String cardNum, String pin) {
        try (Connection conn = DBConnection.getConnection()) {
            // First try login table if it exists
            if (tableExists(conn, "login")) {
                String loginSql = "SELECT 1 FROM login WHERE card_number = ? AND pin = ?";
                try (PreparedStatement ps = conn.prepareStatement(loginSql)) {
                    ps.setString(1, cardNum);
                    ps.setString(2, pin);
                    try (ResultSet rs = ps.executeQuery()) {
                        if (rs.next()) return true;
                    }
                }
            }
            
            // Fallback to signupthree if login table is empty or doesn't exist
            String signupSql = "SELECT 1 FROM signupthree WHERE card_number = ? AND pin = ?";
            try (PreparedStatement ps = conn.prepareStatement(signupSql)) {
                ps.setString(1, cardNum);
                ps.setString(2, pin);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        // If authenticated via signupthree, create login record
                        createLoginRecord(conn, cardNum, pin);
                        return true;
                    }
                }
            }
            return false;
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }

    private boolean tableExists(Connection conn, String tableName) throws SQLException {
        DatabaseMetaData meta = conn.getMetaData();
        try (ResultSet rs = meta.getTables(null, null, tableName, null)) {
            return rs.next();
        }
    }

    private void createLoginRecord(Connection conn, String cardNum, String pin) throws SQLException {
        // Get formno from signupthree to create complete login record
        String formno = getFormnoFromSignup(conn, cardNum);
        if (formno != null) {
            String insertSql = "INSERT INTO login (formno, card_number, pin) VALUES (?, ?, ?)";
            try (PreparedStatement ps = conn.prepareStatement(insertSql)) {
                ps.setString(1, formno);
                ps.setString(2, cardNum);
                ps.setString(3, pin);
                ps.executeUpdate();
            }
        }
    }

    private String getFormnoFromSignup(Connection conn, String cardNum) throws SQLException {
        String sql = "SELECT formno FROM signupthree WHERE card_number = ?";
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, cardNum);
            try (ResultSet rs = ps.executeQuery()) {
                return rs.next() ? rs.getString("formno") : null;
            }
        }
    }

