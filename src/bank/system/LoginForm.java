    private void singInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_singInBtnActionPerformed
        String cardNum = cardNumField.getText().trim();
        String pin = pinField.getText().trim();

        if (cardNum.isEmpty() || pin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter both card number and PIN.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            if (authenticateUser(cardNum, pin)) {
                JOptionPane.showMessageDialog(this, "Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                new Transactions(pin).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid card number or PIN.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Authentication error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_singInBtnActionPerformed

